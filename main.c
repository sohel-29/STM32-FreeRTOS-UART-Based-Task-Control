/* USER CODE BEGIN Header_StartTask03 */
/**
* @brief Function implementing the Task03 thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartTask03 */
void StartTask03(void *argument)
{
  char msg[50];

  for (;;)
  {
    if (HAL_UART_Receive(&huart2, &rxData, 1, 100) == HAL_OK)
    {
      if (rxData == '1')
      {
        activeLed = LED1_SELECTED;
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_6, GPIO_PIN_RESET);

        sprintf(msg, "LED on PA5 selected\r\n");
        UART_SendString(msg);
      }
      else if (rxData == '2')
      {
        activeLed = LED2_SELECTED;
        HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);

        sprintf(msg, "LED on PA6 selected\r\n");
        UART_SendString(msg);
      }
      else
      {
        sprintf(msg, "Invalid input: %c\r\n", rxData);
        UART_SendString(msg);
      }
    }

    osDelay(10);
  }
}

